package junit;

public class Operation {
	 public int add(int a, int b) {
	        return a + b;
	    }
	 
	    public int subtract(int a, int b) {
	        return a - b;
	    }
	    public int mul(int a, int b) {
	        return a * b;
	    }
	 
	    public int divInt(int a, int b) {
	        return a / b;
	    }
}
